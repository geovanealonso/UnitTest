public class Calculadora {

    public int a;
    public int b;

    public int diferenca(int a, int b){
        return a-b;
    }

    public int divisao(int a, int b){
        return a/b;
    }

    public int produto (int a, int b){
        return a*b;
    }

    public int soma (int a, int b){
        return a+b;
    }
}
